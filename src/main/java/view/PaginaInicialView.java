    private void verifyLimitAulas() {
        if (this.index >= aulasMostradas.size()-1) {
            proximo.setEnabled(false);
        } else {
            proximo.setEnabled(true);
        }
        
        if (this.index <= 0) {
            anterior.setEnabled(false);
        } else {
            anterior.setEnabled(true);
        }
    }

